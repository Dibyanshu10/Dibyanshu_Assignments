package com.Dibyanshu.Assignments.Loops;

public class Loops5 
 {
	public static void main(String[] args)
    {
        System.out.println("Odd numbers between 1-100 are :");
        for (int i = 2; i < 100; i++)
        {
            if (i % 2 == 1)
            {
                System.out.println(i);
            }
        }

    }
}
